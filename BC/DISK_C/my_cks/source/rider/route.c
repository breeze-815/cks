void dijkstra(Node *start, Node *end) {
    int distance[MAX_NODES]; // 存储从起点到每个节点的最短距离
    int visited[MAX_NODES] = {0}; // 标记节点是否已访问
    int prev[MAX_NODES]; // 存储每个节点的前驱节点
    int i, j, u, v, min_dist, alt;
    int path[MAX_NODES];
    int path_len = 0;
    int current = end - node;
    char buffer[200];
    int y_offset=50;
    int x1, y1, x2, y2;
    // 初始化
    for (i = 0; i < MAX_NODES; i++) {
        distance[i] = INF;  // 起点到每个节点的最短路径为无穷大
        prev[i] = -1;       // 前驱节点初始化为 -1
    }
    distance[start - node] = 0; // 起点到自身的距离为 0

    // 每一轮 i 循环找到一个节点的最短路径
    for (i = 0; i < MAX_NODES; i++) {
        min_dist = INF;
        u = -1;

        // 找到未访问节点中距离最小的节点
        for (j = 0; j < MAX_NODES; j++) {
            if (!visited[j] && distance[j] < min_dist) {
                min_dist = distance[j];
                u = j; // 记录距离最小节点的索引
            }
        }

        if (u == -1) break; // 所有节点都已访问或不可达
        visited[u] = 1;

        // 更新相邻节点的距离
        for (j = 0; j < node[u].num_of_adj_nodes; j++) {
            v = node[u].adj_nodes[j];
            if (v == 0) continue; // 跳过无效节点
            alt = distance[u] + node[u].distance[j];
            if (alt < distance[v]) {
                distance[v] = alt;
                prev[v] = u;
            }
        }
    }

    // 回溯路径
    while (current != -1 && path_len < MAX_NODES) {
        path[path_len++] = current;
        current = prev[current];
    }

   
    for (i = path_len - 1; i > 0; i--) {
            x1 = node[path[i]].x;
            y1 = node[path[i]].y + 326; // 地图坐标偏移补偿
            x2 = node[path[i - 1]].x;
            y2 = node[path[i - 1]].y + 326;
            Line2(x1, y1, x2, y2, Red); 
            // sprintf(buffer,"Node %d (%d, %d) -> Node %d (%d, %d)", 
            // path[i], node[path[i]].x, node[path[i]].y, 
            // path[i - 1], node[path[i - 1]].x, node[path[i - 1]].y);
            // PrintText(50, y_offset, buffer, HEI, 16, 1, black);
            // y_offset += 15;
    }
    // sprintf(buffer, "Shortest distance: %d", distance[end - node]);
    // PrintText(50, y_offset, buffer, HEI, 16, 1, black);
}


int Manhattan_Distance(int x1, int y1, int x2, int y2) 
{
    return abs(x1 - x2) + abs(y1 - y2);
}

